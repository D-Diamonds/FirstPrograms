/*
* OneLine class
*
* This prints out certain strings in one line
*
* Author: Josh Landsman
*/
public class OneLine {
    public static void main(String[] args) {
        //Prints 1-5
        System.out.println("I can count to five:\n1 2 3 4 5");
        //Prints 1-5 in 8-digit binary
        System.out.println("I can also count in eight-digit binary:\n" + "00000001\n" + "00000010\n" + "00000011\n" + "00000100\n" + "00000101");
        //Prints a half pyramid
        System.out.println("*\n" + "**\n" + "***\n" + "****\n" + "*****\n");
        //Prints an upside down half pyramid
        System.out.println("*****\n" + "****\n" + "***\n" + "**\n" + "*");
    }
}
